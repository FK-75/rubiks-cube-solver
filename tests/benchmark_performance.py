"""
benchmark_performance.py

Benchmarks the Kociemba solver across randomly scrambled (but always valid)
cube states, generated by applying random moves to a solved cube.

Usage:
    python benchmark_performance.py
    python benchmark_performance.py --iterations 200
"""

import time
import random
import platform
import argparse
import sys

try:
    import kociemba
except ImportError:
    print("ERROR: kociemba is not installed. Run: pip install kociemba")
    sys.exit(1)

ALL_MOVES = ["U", "U'", "U2", "D", "D'", "D2",
             "R", "R'", "R2", "L", "L'", "L2",
             "F", "F'", "F2", "B", "B'", "B2"]

FACE_ORDER = ['U', 'R', 'F', 'D', 'L', 'B']


def make_solved():
    return {f: [f] * 9 for f in FACE_ORDER}


def cube_to_string(state):
    return ''.join(''.join(state[f]) for f in FACE_ORDER)


def apply_move(state, move):
    """Apply a single move to a cube state dict and return the new state."""
    s = {f: list(state[f]) for f in FACE_ORDER}
    face = move[0]
    ccw  = "'" in move
    reps = 2 if '2' in move else 1

    def rot_cw(f):
        p = s[f]
        s[f] = [p[6],p[3],p[0],p[7],p[4],p[1],p[8],p[5],p[2]]

    def rot_ccw(f):
        p = s[f]
        s[f] = [p[2],p[5],p[8],p[1],p[4],p[7],p[0],p[3],p[6]]

    def cycle4(a,ai, b,bi, c,ci, d,di):
        """Cycle four stickers: a <- d <- c <- b <- a (clockwise move)."""
        tmp = s[a][ai]
        s[a][ai] = s[d][di]
        s[d][di] = s[c][ci]
        s[c][ci] = s[b][bi]
        s[b][bi] = tmp

    for _ in range(reps):
        rot_ccw(face) if ccw else rot_cw(face)

        # Edge sticker positions for each face, listed clockwise
        if face == 'U':
            for i in range(3):
                if ccw: cycle4('B',i, 'L',i, 'F',i, 'R',i)
                else:   cycle4('B',i, 'R',i, 'F',i, 'L',i)

        elif face == 'D':
            for i in range(3):
                if ccw: cycle4('F',6+i, 'L',6+i, 'B',6+i, 'R',6+i)
                else:   cycle4('F',6+i, 'R',6+i, 'B',6+i, 'L',6+i)

        elif face == 'F':
            ring = [('U',6),('U',7),('U',8),
                    ('R',0),('R',3),('R',6),
                    ('D',2),('D',1),('D',0),
                    ('L',8),('L',5),('L',2)]
            for i in range(3):
                a,b,c,d = ring[i], ring[i+3], ring[i+6], ring[i+9]
                if ccw: cycle4(a[0],a[1], d[0],d[1], c[0],c[1], b[0],b[1])
                else:   cycle4(a[0],a[1], b[0],b[1], c[0],c[1], d[0],d[1])

        elif face == 'B':
            ring = [('U',2),('U',1),('U',0),
                    ('L',0),('L',3),('L',6),
                    ('D',6),('D',7),('D',8),
                    ('R',8),('R',5),('R',2)]
            for i in range(3):
                a,b,c,d = ring[i], ring[i+3], ring[i+6], ring[i+9]
                if ccw: cycle4(a[0],a[1], d[0],d[1], c[0],c[1], b[0],b[1])
                else:   cycle4(a[0],a[1], b[0],b[1], c[0],c[1], d[0],d[1])

        elif face == 'R':
            ring = [('U',2),('U',5),('U',8),
                    ('B',6),('B',3),('B',0),
                    ('D',2),('D',5),('D',8),
                    ('F',2),('F',5),('F',8)]
            for i in range(3):
                a,b,c,d = ring[i], ring[i+3], ring[i+6], ring[i+9]
                if ccw: cycle4(a[0],a[1], d[0],d[1], c[0],c[1], b[0],b[1])
                else:   cycle4(a[0],a[1], b[0],b[1], c[0],c[1], d[0],d[1])

        elif face == 'L':
            ring = [('U',0),('U',3),('U',6),
                    ('F',0),('F',3),('F',6),
                    ('D',0),('D',3),('D',6),
                    ('B',8),('B',5),('B',2)]
            for i in range(3):
                a,b,c,d = ring[i], ring[i+3], ring[i+6], ring[i+9]
                if ccw: cycle4(a[0],a[1], d[0],d[1], c[0],c[1], b[0],b[1])
                else:   cycle4(a[0],a[1], b[0],b[1], c[0],c[1], d[0],d[1])
    return s


def generate_scramble(num_moves=20):
    """Return a valid 54-char Kociemba string from a randomly scrambled cube."""
    state = make_solved()
    for move in random.choices(ALL_MOVES, k=num_moves):
        state = apply_move(state, move)
    return cube_to_string(state)


def get_hardware_info():
    uname = platform.uname()
    cpu = uname.processor or uname.machine
    return f"{uname.machine} — {cpu}, {uname.system} {uname.release}"


def run_benchmarks(iterations: int = 100):
    print(f"\nGenerating {iterations} valid scrambled states and benchmarking...")
    print("=" * 55)

    solve_times = []
    move_counts = []
    failed = 0

    for i in range(iterations):
        state = generate_scramble(num_moves=random.randint(15, 25))
        try:
            start = time.perf_counter()
            solution = kociemba.solve(state)
            end = time.perf_counter()
            solve_times.append(end - start)
            move_counts.append(len(solution.split()))
        except Exception as e:
            failed += 1

        if (i + 1) % 25 == 0:
            print(f"  {i + 1}/{iterations} completed...")

    if not solve_times:
        print("No successful solves — check your kociemba installation.")
        return

    avg_time  = sum(solve_times) / len(solve_times)
    min_time  = min(solve_times)
    max_time  = max(solve_times)
    avg_moves = sum(move_counts) / len(move_counts)
    min_moves = min(move_counts)
    max_moves = max(move_counts)
    hardware  = get_hardware_info()

    print("\n" + "=" * 55)
    print("RESULTS")
    print("=" * 55)
    print(f"  Iterations      : {iterations}  (failed: {failed})")
    print(f"  Avg solve time  : {avg_time*1000:.2f} ms")
    print(f"  Min solve time  : {min_time*1000:.2f} ms")
    print(f"  Max solve time  : {max_time*1000:.2f} ms")
    print(f"  Avg move count  : {avg_moves:.1f}")
    print(f"  Move range      : {min_moves}–{max_moves}")
    print(f"  Hardware        : {hardware}")
    print("=" * 55)

    print("\n--- Copy the block below into your README ---\n")
    print("```")
    print(f"Average solve time : {avg_time*1000:.1f} ms  (min {min_time*1000:.1f} ms / max {max_time*1000:.1f} ms)")
    print(f"Average move count : {avg_moves:.0f} moves  (range {min_moves}–{max_moves})")
    print(f"Algorithm          : Kociemba two-phase")
    print(f"Hardware           : {hardware}")
    print("```")
    print()


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Benchmark Kociemba solve performance.")
    parser.add_argument("--iterations", type=int, default=100,
                        help="Number of solves to run (default: 100)")
    args = parser.parse_args()
    run_benchmarks(args.iterations)
